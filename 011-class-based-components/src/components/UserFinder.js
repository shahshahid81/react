class UserFinder extends Component {
  // Can only use one context per component. To use multiple, need to use other work arounds
  static contextType = UsersContext;

  constructor() {
    super();

    this.state = {
      filteredUsers: [],
      searchTerm: ''
    };
  }

  searchChangeHandler(event) {
    this.setState({
      searchTerm: event.target.value
    });
  };

  // This will run once the component is mounted to the DOM
  componentDidMount() {
    this.setState({ filteredUsers: this.context.users });
  }

  // This will execute every time the component is executed
  componentDidUpdate(prevProps, prevState) {
    // Added the check to avoid infinite loop
    if (prevState.searchTerm !== this.state.searchTerm) {
      this.setState({
        filteredUsers: this.context.users.filter((user) => user.name.includes(this.state.searchTerm))
      })
    }
  }

  render() {
    return (
      <Fragment>
        <div className={classes.finder}>
          <input type='search' onChange={this.searchChangeHandler.bind(this)} />
        </div>
        <ErrorBoundary>
          <Users users={this.state.filteredUsers} />
        </ErrorBoundary>
      </Fragment>
    );
  }
};

